print(f'Multiplicação: {a} * {b} = \033[1;36m{multiplicacao}\033[m')
print(f'Divisão: {a} / {b} = \033[1;36m{divisao}\033[m')
print(f'Resto: {a} % {b} = \033[1;36m{resto}\033[m')
print(f'Divisão inteira: {a} // {b} = \033[1;36m{divisao_inteira}\033[m')
print(f'Potência: {a} ** {b} = \033[1;36m{potencia}\033[m')
